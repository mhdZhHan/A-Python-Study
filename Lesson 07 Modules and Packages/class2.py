# Math Function

x = -19

# return absolute value
print(abs(x)) # 19

# rounding
print(round(234.98547, 2)) # 234.98

# power of a number
print(pow(4, 3)) # 64

# bigest number in a list
print(max(1,34, 56, 2,87)) # 87

# smallest number in a list
print(min(1,34, 56, 2,87)) # 1

# ========== Math module ==========

import math


print(math.pow(4,2)) # 16

print(math.sqrt(16)) # 4