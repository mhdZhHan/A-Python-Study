# Logical Operations with Boolean

# ====================== 1. and ========================

print(3 < 5 and 20 > 17) # True

# a = True and Tru     # True
# b = True and Fals    # False
# c = False and Fals   # False
# d = False and Tru    # False

# ====================== 1. or ========================

print(1 > 10 or 5 < 8) # True

# a = True or True      # True
# b = True or False     # True
# c = False or False    # False
# d = False or True     # True

# ====================== 1. not ========================

to_be = True

print(not to_be) # False

# to_be = True               # to_be is True
# not_to_be = not to_be      # not_t_be is False