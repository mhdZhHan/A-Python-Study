# Type Casting

# ============================= 1. int() ===========================

x = "123"

# return the type of the string 'x'
print(type(x)) # <class 'str'>

# convert teh string 'x' into integer
print(type(int(x))) # <class 'int'>

# ============================= 1. str() ===========================

y = 10

# return the type of the integer 'y'
print(type(y)) # <class 'int'>

# convert teh integer 'y' into string
print(type(str(y))) # <class 'str'>

# ============================= 1. float() ===========================

z = 7

# return the type of the integer 'z'
print(type(z))

# convert teh integer 'z' into float
print(type(float(z)))

# ============== float() Eg: ==================
num = 7

float_num = float(num)

print(float_num) # 7.0